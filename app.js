// 전체 키워드 추출 (중복 제거, 빈도순 정렬)
function getAllKeywords() {
  const freq = {};
  insights.forEach(item => {
    item.keywords.forEach(kw => {
      freq[kw] = (freq[kw] || 0) + 1;
    });
  });
  return Object.entries(freq)
    .sort((a, b) => b[1] - a[1])
    .map(([kw]) => kw);
}

